# Define display_board that accepts a board and prints
# out the current state.
def display_board arr
  puts " #{arr[0]} |"
  puts " #{arr[1]} |"
  puts " #{arr[2]} "
  puts "-----------"
  puts " #{arr[3]} |"
  puts " #{arr[4]} |"
  puts " #{arr[5]} "
  puts "-----------"
  puts " #{arr[6]} |"
  puts " #{arr[7]} |"
  puts " #{arr[8]}"
end
